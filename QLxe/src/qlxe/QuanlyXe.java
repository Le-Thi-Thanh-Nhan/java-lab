    private boolean check(String maxe) throws ClassNotFoundException {
        boolean check = false;
        try {
            Connect pt = new Connect();

            ResultSet rs = pt.GetData("Products");
            while (rs.next()) {
                if (rs.getString(1).equals(maxe) == true) {
                    check = true;
                }
            }
            pt.Close();
        } catch (SQLException sqlException) {
            sqlException.printStackTrace();
                }
        return check;
    }    
    
    
