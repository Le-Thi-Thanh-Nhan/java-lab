    private void jBthemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBthemMouseClicked
        // TODO add your handling code here:
        jTmaxe.setEnabled(true);
        String maxe = jTmaxe.getText();
        String tenxe = jTtenxe.getText();
        String hangxe = jThangxe.getText();
        String giathanh = jTgiathanh.getText();
        maxe = maxe.replaceAll(" ", "");
        tenxe = tenxe.replaceAll(" ", "");
        hangxe = hangxe.replaceAll(" ", "");
        giathanh = giathanh.replaceAll(" ", "");
        if (jTmaxe.getText().equals("") == true || jTtenxe.equals("") == true || jTgiathanh.equals("") == true) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập dữ liệu!");
        } else {
            try {
                if (check(maxe) == true) {
                    JOptionPane.showMessageDialog(null, "Mã sản phẩm bị trùng!!! Vui lòng nhập lại!!!");
                } else {
                    Insert();
                    ShowTable();
                    JOptionPane.showMessageDialog(null, "Đã thêm thành công");
                    
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(QuanlyXe.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBthemMouseClicked

