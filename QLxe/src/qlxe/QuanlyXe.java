    //Đếm số dòng có trong bảng Products
    public int Dem() throws ClassNotFoundException {
        int dem = 0;
        try {
            Connect pt = new Connect();
            ResultSet rs = pt.GetData("Products");
            while (rs.next()) {
                dem++;//Đếm số dòng trong table Products
            }

            pt.Close();
        } catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }
        return dem;
    }

    public void ShowTable() {
      //  Hiển thị dữ liệu lên table;
        DefaultTableModel model = (DefaultTableModel) jTqlxe.getModel();
         model.setRowCount(0);
         model.setRowCount(50);
        // model.setRowCount(Dem());//Thiết lập số dòng của jtable = số dòng của bảng của DataBase
        try {
            Connect pt = new Connect();
            ResultSet rs = pt.GetData("Products");
            int i = 0;
            while (rs.next()) {                
                jTqlxe.setValueAt(rs.getString("ID"), i, 0);
                jTqlxe.setValueAt(rs.getString("TenXe"), i, 1);
                jTqlxe.setValueAt(rs.getString("HangXe"), i, 2);
                jTqlxe.setValueAt(rs.getString("GiaThanh"), i, 3);
                i++;
            }
            pt.Close();
        } catch (SQLException sqlException) {
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QuanlyXe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
